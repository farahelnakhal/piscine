#include <unistd.h>

int	main(int argc, char *arg[])
{
	if (argc != 2 || arg[1][0] == '\0')
	{
		write(1, "\n", 1);
		return (0);
	}
	int	i;

	i = 0;
	while (arg[1][i] != '\0')
	{
		if (arg[1][i] == 90 || arg[1][i] == 122)
		{
			arg[1][i] -= 25;
		}
		else
		{
			arg[1][i] += 1;
		}
		write(1, &(arg[1][i]), 1);
		i++;
	}
	return (0);
}
