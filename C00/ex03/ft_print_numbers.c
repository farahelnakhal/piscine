#include <unistd.h>

void	ft_print_numbers(void)
{
	char	buffer[10];
	int		a;

	a = 0;
	while (a != 10)
	{
		buffer[a] = a + '0';
		a += 1;
	}
	write(1, buffer, 10);
}
